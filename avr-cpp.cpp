#include <avr/io.h>
#include "avr-cpp.h"

#include <inttypes.h>

void * operator new(size_t size) 
{ 
  return malloc(size); 
} 

void operator delete(void * ptr) 
{ 
  free(ptr); 
} 

void __cxa_pure_virtual(void) {}; 

uint8_t strlen(char *str)
{
  uint8_t ret = 0;
  while(*str++ != 0)
    ret++;
  return ret;
}
